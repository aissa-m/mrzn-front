import React, { useState } from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  StyleSheet,
  Alert,
  ActivityIndicator,
} from 'react-native';
import { authService } from '../services/auth';

export default function LoginScreen() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);

  const handleLogin = async () => {
    if (!email || !password) {
      Alert.alert('Error', 'Por favor completa todos los campos');
      return;
    }

    try {
      setLoading(true);
      const response = await authService.login({ email, password });
      
      Alert.alert('xito', `隆Bienvenido ${response.user.name}!`);
      // TODO: Navegar a Home (pr贸ximo paso)
    } catch (error: any) {
      Alert.alert(
        'Error',
        error.response?.data?.message || 'Credenciales inv谩lidas'
      );
    } finally {
      setLoading(false);
    }
  };

  return (
    <View style={styles.container}>
      <Text style={styles.title}>Maurizone </Text>
      <Text style={styles.subtitle}>Inicia sesi贸n</Text>

      <TextInput
        style={styles.input}
        placeholder="Email"
        value={email}
        onChangeText={setEmail}
        keyboardType="email-address"
        autoCapitalize="none"
        editable={!loading}
      />

      <TextInput
        style={styles.input}
        placeholder="Contrase帽a"
        value={password}
        onChangeText={setPassword}
        secureTextEntry
        editable={!loading}
      />

      <TouchableOpacity
        style={[styles.button, loading && styles.buttonDisabled]}
        onPress={handleLogin}
        disabled={loading}
      >
        {loading ? (
          <ActivityIndicator color="#fff" />
        ) : (
          <Text style={styles.buttonText}>Iniciar Sesi贸n</Text>
        )}
      </TouchableOpacity>

      <TouchableOpacity onPress={() => Alert.alert('Pr贸ximamente', 'Funci贸n de registro')}>
        <Text style={styles.link}>驴No tienes cuenta? Reg铆strate</Text>
      </TouchableOpacity>
    </View>
  );
}

